/**
 * ====================================================================
 * Патерн: Strategy (Поведінковий / Behavioral) — Інтерфейс стратегії
 * ====================================================================
 * Визначає контракт для всіх алгоритмів сортування завдань.
 * Кожна конкретна стратегія реалізує метод `sort()` по-своєму,
 * а контекст (TaskSortContext) делегує виклик обраній стратегії.
 *
 * Це дозволяє фронтенду динамічно підміняти алгоритм сортування
 * через query-параметр `?sort=deadline|priority|title`.
 * ====================================================================
 */
import { TaskEntity } from '../../task.entity';

/** Інтерфейс стратегії — контракт для всіх алгоритмів сортування */
export interface ITaskSortStrategy {
  /** Сортує масив завдань та повертає новий відсортований масив */
  sort(tasks: TaskEntity[]): TaskEntity[];
}
