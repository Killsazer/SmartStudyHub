/**
 * ====================================================================
 * Патерн: Observer (Поведінковий / Behavioral) — Інтерфейси
 * ====================================================================
 * Визначає контракти для реактивного механізму сповіщень.
 * IObservable (Суб'єкт) — об'єкт, за яким спостерігають.
 * IObserver (Спостерігач) — об'єкт, який реагує на зміни.
 *
 * Обидва інтерфейси generic (`<T>`), що дозволяє використовувати
 * патерн для будь-яких типів даних, а не лише TaskEntity.
 * ====================================================================
 */

/** Спостерігач — реагує на оновлення від суб'єкта */
export interface IObserver<T> {
  /** Викликається суб'єктом при зміні стану */
  update(data: T): void;
}

/** Суб'єкт (Observable) — управляє списком спостерігачів та розсилає оновлення */
export interface IObservable<T> {
  /** Додає спостерігача до списку підписників */
  attach(observer: IObserver<T>): void;
  /** Видаляє спостерігача зі списку підписників */
  detach(observer: IObserver<T>): void;
  /** Сповіщує всіх підписаних спостерігачів про зміну стану */
  notify(data: T): void;
}
