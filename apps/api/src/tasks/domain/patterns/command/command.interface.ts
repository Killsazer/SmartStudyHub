/**
 * ====================================================================
 * Патерн: Command (Поведінковий / Behavioral) — Інтерфейс команди
 * ====================================================================
 * Визначає контракт для всіх команд: кожна команда повинна вміти
 * виконатися (`execute`) та скасуватися (`undo`).
 * Це дозволяє інкапсулювати дії як об'єкти та зберігати історію.
 * ====================================================================
 */
export interface ICommand {
  /** Виконує дію, інкапсульовану в команді */
  execute(): void;
  /** Скасовує дію, відкочуючи стан до попереднього */
  undo(): void;
}
